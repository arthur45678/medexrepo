                        <li>Կատարողի կողմից մատուցված ծառայություններում հայտնաբերված թերությունները վերացնելուց հրաժարվելու կամ այդ թերությունները ոչ պատշաճ ձևով վերացնելու դեպքում իր ուժերով վերացնել դրանք և իր կրած ծախսերին համաչափ նվազեցնել Կատարողին վճարելիք գինը, եթե դեռևս ամբողջությամբ չի վճարվել, իսկ եթե վճարվել է ամբողջությամբ, ապա պահանջել հատուցելու հայտնաբերված թերությունները վերացնելու համար իր կատարած ծախսերը և կրած այլ վնասները.</li>
                        <li>Ցանկացած պահի հրաժարվել Պայմանագրից` Կատարողին վճարելով արդեն իսկ մատուցված ծառայությունների համար գնացուցակի հիման վրա հաշվարկված գինը.</li>
                        <li>Ստանալ տեղեկատվություն կատարողի կողմից մատուցվող ծառայությունների գների վերաբերյալ դրանց մատուցման ցանկացած փուլում.</li>
                        <li> Ընտրել Կատարողի անունից իրեն անմիջականորեն ծառայություններ մատուցող բժշկին.</li>
                        <li>Ծանոթանալ Կատարողի ներքին կարգապահական կանոններին.</li>
                        <li>Տեղեկատվություն ստանալ իրեն անմիջականորեն ծառայություններ մատուցող բուժաշխատողների որակավորման մասին.</li>
                        <li>Պահանջել բժշկի օգնությանը դիմելու փաստի, իր առողջական վիճակի, հետազոտման, ախտորոշման և բուժման ընթացքում պարզված տեղեկությունների գաղտնիության ապահովում, բացի ՀՀ օրենսդրությամբ սահմանված դեպքերի.</li>
                        <li>Անմիջապես Կատարողից ճշտել ծառայությունների մատուցման ընթացքը.</li>
                        <li>Իրականացնել ՀՀ օրենսդրությամբ («Բնակչության բժշկական օգնության և սպասարկման մասին» ՀՀ օրենք և այլն) և Պայմանագրով նախատեսված այլ իրավունքներ:</li>
                    </ul>
                    <div>&nbsp;&nbsp;&nbsp;&nbsp;3.2. Պատվիրատուն պարտավոր է.</div>
                    <ul>
                        <li>Վճարել պատշաճ որակով և Պայմանագրին համապատասխան որակով մատուցված ծառայությունների դիմաց` Պայմանագրով սահմանված ժամկետում և չափով.</li>
                        <li>Ծառայությունների մատուցման ընթացքի վերաբերյալ ճշտումներ իրականացնելիս չխոչընդոտել կատարողի կողմից իր պարտականությունների իրականացմանը.</li>
                        <li>Հետևել բժշկի ցուցումներին.</li>
                        <li>Կատարողին տրամադրել իրեն հայտնի տեղեկություններ իր առողջության վերաբերյալ.</li>
                        <li>Կատարողին անհապաղ տեղյակ պահել իր առողջական վիճակի ցանկացած փոփոխության մասին.</li>
                        <li>Ծառայությունների մատուցման ընթացքում, ինչպես նաև դրանից հետո կատարել Կատարողի ցուցումները` կապված ծառայությունների արդյունքի պահպանման հետ` ներառյալ Կատարողին անհրաժեշտ տեղեկատվության տրամադրումը.</li>
                        <li>Փոխհատուցել իր կողմից կատարողի գույքին պատճառված վնասը.</li>
                        <li>Պահպանել Կատարողի կողմից ընդունված իրավական ակտերով հաստատված ներքին կարգապահական կանոնները.</li>
                        <li>Իրականացնել ՀՀ օրենսդրությամբ և պայմանագրով սահմանված այլ պարտականություններ:</li>
                    </ul>
                    <div>&nbsp;&nbsp;&nbsp;&nbsp;3.3. Կատարողն իրավունք ունի.</div>
                    <ul>
                        <li>Պահանջել պատվիրատուից վճարել իր կողմից Պայմանագրով նախատեսված ծառայությունների պատշաճ մատուցման համար` սահմանված ձևով, չափով և ժամկետներում.</li>
                        <li>Պատվիրատուի կողմից իր պարտավորությունների կատարումից անհիմն հրաժարվելու դեպքում, պահանջել իր կրած վնասների հատուցումը.</li>
                        <li>Հրաժարվել Պատվիրատուին սույն պայմանագրով նախատեսված ծառայությունը մատուցելուց, եթե Պատվիրատուն չի կատարում կամ ոչ պատշաճ է կատարում սույն պայմանագրով նախատեսված պարտականությունները, բացառությամբ Պատվիրատուի կյանքին վտանգ սպառնացող դեպքերի.</li>
                        <li>Անհրաժեշտության դեպքում պատվիրատուի համաձայնությամբ ծառայությունների մատուցմանը ներգրավել բժշկական օգնության և սպասարկման ծառայություններ մատուցող ենթակատարողների.</li>
                        <li>Իրականացնել ՀՀ օրենսդրությամբ և պայմանագրով սահմանված այլ իրավունքներ:</li>
                    </ul>
