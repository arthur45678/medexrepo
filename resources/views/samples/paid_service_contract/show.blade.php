                    <div>&nbsp;&nbsp;&nbsp;&nbsp;3.4. Կատարողը պարտավոր է.</div>
                    <ul>
                        <li>Պայմանագրի 1.2 կետով սահմանված ծառայությունները պատվիրատուին մատուցել պատշաճ որակով և ժամանակին:</li>
                        <li>Սույն պայմանագրի կնքման ժամանակ Պատվիրատուին հայերեն լեզվով (եթե Պատվիրատուն չի տիրապետում հայերեն լեզվին, ապա վերջինիս հասկանալի լեզվով) տրամադրել ստույգ տեղեկատվություն իր կողմից մատուցվող ծառայությունների վերաբերյալ անհրաժեշտ տեղեկատվություն` ապահովելով Պատվիրատուի կողմից դրանց ճիշտ ընտրության հնարավորությունը:</li>
                        <li>Պատվիրատուի պահանջով և վերջինիս կողմից սահմանված ժամկետում անհատույց վերացնել բուժման ընթացքում հայտնաբերված թերությունները.</li>
                        <li>Պայմանագրով նախատեսված բոլոր ծառայությունները մատուցել իր միջոցներով և իր հաշվին.</li>
                        <li>Պատվիրատուին ծանոթացնել բժշկական օգնություն և սպասարկում ստանալիս մարդու իրավունքների մասին</li>
                        <li>Ապահովել Պատվիրատուի կողմից բժշկի օգնությանը դիմելու փաստի, նրա առողջական վիճակի, հետազոտման, ախտորոշման և բուժման ընթացքում պարզված տեղեկությունների գաղտնիությունը, բացի ՀՀ օրենսդրությամբ սահմանված դեպքերի.</li>
                        <li>Մատուցված ծառայությունների արդյունքի պահպանման վերաբերյալ Պատվիրատուին մատչելի ձևով տրամադրել անհրաժեշտ տեղեկատվություն.</li>
                        <li>Պատվիրատուին ծանոթացնել իր կողմից հաստատված ներքին կարգապահական կանոններին.</li>
                        <li>Սույն պայմանագրով նախատեսված ծառայությունների մատուցման ընթացքի վերաբերյալ վարել համապատասխան բժշկական փաստաթղթեր.</li>
                        <li>իրականացնել ՀՀ օրենսդրությամբ և Պայմանագրում սահմանված այլ պարտականություններ:</li>
                    </ul>
